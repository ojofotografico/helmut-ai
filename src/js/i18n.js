window.setLanguage = function(lang) {
    localStorage.setItem('helmut-lang', lang);
    document.documentElement.lang = lang;
    const elements = document.querySelectorAll('[data-i18n]');
    
    console.log(`[Helmut AI] Sincronizando ${elements.length} nodos a: ${lang}`);

    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // Usamos innerHTML para permitir etiquetas como <br> si fuera necesario
            el.innerHTML = translations[lang][key]; 
        } else {
            console.warn(`[Helmut AI] Llave faltante: ${key} en idioma: ${lang}`);
        }
    });
};